    public void loadData() { //menampilkan data yang ada di database
        try {
            //membuat koneksi
            Connection conn = KoneksiDB.getKoneksi();
            Statement s = conn.createStatement();
            //membuat query select
            String sql = "SELECT * FROM penjualan";
            ResultSet r = s.executeQuery(sql);
            
            //membaca di dalam database baris per baris
            while(r.next()) {
                //membuat objek 'obj' untuk menampung data dari database
                Object[] obj = new Object[5];
                obj[0] = r.getString("id_penjualan");
                obj[1] = r.getString("nama_barang");
                obj[2] = r.getString("jumlah_beli");
                obj[3] = r.getString("harga");
                obj[4] = r.getString("total_bayar");
                //tampilkan satu baris dalam tabel
                model.addRow(obj);
            }
            //menutup penelusuran dan koneksi
            r.close();
            s.close();
        }
        catch (SQLException e) {
            System.out.println("Terjadi error");
        }
    }

