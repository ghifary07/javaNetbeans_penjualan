    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Simpan data ke database
        int jumlahbeli = Integer.parseInt(jTextField3.getText());
        int harga      = Integer.parseInt(jTextField4.getText());
        
        int totalbayar = total(jumlahbeli, harga);
        
        try {
            Connection conn = KoneksiDB.getKoneksi();
            
            String sql = "INSERT INTO penjualan (nama_barang,jumlah_beli,harga,"
                    + "total_bayar) VALUES(?,?,?,?)";
            PreparedStatement p = conn.prepareStatement(sql);
            
            p.setString(1, jTextField2.getText());
            p.setInt(2, Integer.parseInt(jTextField3.getText()));
            p.setInt(3, Integer.parseInt(jTextField4.getText()));
            p.setInt(4, totalbayar);
            
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(rootPane, "Data tersimpan");

        }catch (SQLException e) {
            System.out.println("Terjadi error");
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

